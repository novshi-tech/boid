func createTaskForTest(t *testing.T, ts *testutil.TestServer, projectID, title string) orchestrator.Task {
	t.Helper()
	req := map[string]any{
		"project_id": projectID,
		"title":      title,
		"behavior":   "planning",
	}
	var task orchestrator.Task
	if err := ts.Client.Do("POST", "/api/tasks", req, &task); err != nil {
		t.Fatalf("create task %q: %v", title, err)
	}
	return task
}

func TestCreateTask_WithSpecifiedID(t *testing.T) {
	ts := testutil.NewTestServer(t)
	createProjectWithBehavior(t, ts, "proj-id-test", "ID Test")

	req := map[string]any{
		"id":         "my-specific-task-id",
		"project_id": "proj-id-test",
		"title":      "Task with specified ID",
		"behavior":   "planning",
	}
	var task orchestrator.Task
	if err := ts.Client.Do("POST", "/api/tasks", req, &task); err != nil {
		t.Fatalf("POST /api/tasks: %v", err)
	}
	if task.ID != "my-specific-task-id" {
		t.Fatalf("ID = %q, want %q", task.ID, "my-specific-task-id")
	}

	// Verify via GET
	var got orchestrator.Task
	if err := ts.Client.Do("GET", "/api/tasks/my-specific-task-id", nil, &got); err != nil {
		t.Fatalf("GET /api/tasks/my-specific-task-id: %v", err)
	}
	if got.ID != "my-specific-task-id" {
		t.Fatalf("GET ID = %q, want %q", got.ID, "my-specific-task-id")
	}
}

