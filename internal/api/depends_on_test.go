func TestCreateTask_WithDependsOn(t *testing.T) {
	ts := testutil.NewTestServer(t)
	createProjectWithBehavior(t, ts, "proj-dep-test", "Dep Test")

	dep1 := createTaskForTest(t, ts, "proj-dep-test", "Dep 1")
	dep2 := createTaskForTest(t, ts, "proj-dep-test", "Dep 2")

	req := map[string]any{
		"project_id": "proj-dep-test",
		"title":      "Main task",
		"behavior":   "planning",
		"depends_on": []string{dep1.ID, dep2.ID},
	}
	var task orchestrator.Task
	if err := ts.Client.Do("POST", "/api/tasks", req, &task); err != nil {
		t.Fatalf("POST /api/tasks: %v", err)
	}
	if len(task.DependsOn) != 2 {
		t.Fatalf("DependsOn = %v (len %d), want 2 items", task.DependsOn, len(task.DependsOn))
	}

	// Verify DependsOn is loaded on subsequent GET
	var got orchestrator.Task
	if err := ts.Client.Do("GET", "/api/tasks/"+task.ID, nil, &got); err != nil {
		t.Fatalf("GET /api/tasks/%s: %v", task.ID, err)
	}
	if len(got.DependsOn) != 2 {
		t.Fatalf("GET DependsOn = %v (len %d), want 2 items", got.DependsOn, len(got.DependsOn))
	}
	found1, found2 := false, false
	for _, id := range got.DependsOn {
		if id == dep1.ID {
			found1 = true
		}
		if id == dep2.ID {
			found2 = true
		}
	}
	if !found1 || !found2 {
		t.Fatalf("DependsOn = %v, want contains [%s, %s]", got.DependsOn, dep1.ID, dep2.ID)
	}
}

