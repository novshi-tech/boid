func TestCreateTask_WithSpecifiedID(t *testing.T) {
	d := createTestProject(t)

	task := &orchestrator.Task{
		ID:        "my-custom-id-1234",
		ProjectID: "proj-1",
		Title:     "Task with custom ID",
		Behavior:  "dev",
	}
	if err := orchestrator.CreateTask(d.Conn, task); err != nil {
		t.Fatalf("CreateTask() error = %v", err)
	}
	if task.ID != "my-custom-id-1234" {
		t.Fatalf("ID = %q, want %q", task.ID, "my-custom-id-1234")
	}

	got, err := orchestrator.GetTask(d.Conn, "my-custom-id-1234")
	if err != nil {
		t.Fatalf("GetTask() error = %v", err)
	}
	if got.ID != "my-custom-id-1234" {
		t.Fatalf("ID = %q, want %q", got.ID, "my-custom-id-1234")
	}
}

