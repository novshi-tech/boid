func TestCreateTask_WithDependsOn(t *testing.T) {
	d := createTestProject(t)

	dep1 := &orchestrator.Task{ProjectID: "proj-1", Title: "Dep 1", Behavior: "dev"}
	dep2 := &orchestrator.Task{ProjectID: "proj-1", Title: "Dep 2", Behavior: "dev"}
	if err := orchestrator.CreateTask(d.Conn, dep1); err != nil {
		t.Fatalf("create dep1: %v", err)
	}
	if err := orchestrator.CreateTask(d.Conn, dep2); err != nil {
		t.Fatalf("create dep2: %v", err)
	}

	task := &orchestrator.Task{
		ProjectID: "proj-1",
		Title:     "Task with dependencies",
		Behavior:  "dev",
		DependsOn: []string{dep1.ID, dep2.ID},
	}
	if err := orchestrator.CreateTask(d.Conn, task); err != nil {
		t.Fatalf("CreateTask() error = %v", err)
	}

	got, err := orchestrator.GetTask(d.Conn, task.ID)
	if err != nil {
		t.Fatalf("GetTask() error = %v", err)
	}
	if len(got.DependsOn) != 2 {
		t.Fatalf("DependsOn = %v (len %d), want 2 items", got.DependsOn, len(got.DependsOn))
	}
	// DependsOn is sorted by depends_on column
	foundDep1, foundDep2 := false, false
	for _, id := range got.DependsOn {
		if id == dep1.ID {
			foundDep1 = true
		}
		if id == dep2.ID {
			foundDep2 = true
		}
	}
	if !foundDep1 || !foundDep2 {
		t.Fatalf("DependsOn = %v, want [%s, %s]", got.DependsOn, dep1.ID, dep2.ID)
	}
}

