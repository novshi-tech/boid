func TestDeleteTask_AlsoCleansTaskDependencies(t *testing.T) {
	d := testutil.NewTestDB(t)
	if err := orchestrator.CreateProject(d.Conn, &orchestrator.Project{ID: "proj-1", WorkDir: "/tmp"}); err != nil {
		t.Fatalf("create project: %v", err)
	}

	dep := &orchestrator.Task{ProjectID: "proj-1", Title: "Dep", Behavior: "dev"}
	if err := orchestrator.CreateTask(d.Conn, dep); err != nil {
		t.Fatalf("create dep: %v", err)
	}
	task := &orchestrator.Task{
		ProjectID: "proj-1",
		Title:     "Task",
		Behavior:  "dev",
		DependsOn: []string{dep.ID},
	}
	if err := orchestrator.CreateTask(d.Conn, task); err != nil {
		t.Fatalf("create task: %v", err)
	}

	// Delete the dependent task — should also remove from task_dependencies
	if err := orchestrator.DeleteTask(d.Conn, task.ID); err != nil {
		t.Fatalf("DeleteTask() error = %v", err)
	}

	// dep should still be deletable (no FK violation)
	if err := orchestrator.DeleteTask(d.Conn, dep.ID); err != nil {
		t.Fatalf("DeleteTask(dep) after deleting task: %v", err)
	}
}

func TestDeleteTask_DepIsDeleted_DependentsCleanedUp(t *testing.T) {
	d := testutil.NewTestDB(t)
	if err := orchestrator.CreateProject(d.Conn, &orchestrator.Project{ID: "proj-1", WorkDir: "/tmp"}); err != nil {
		t.Fatalf("create project: %v", err)
	}

	dep := &orchestrator.Task{ProjectID: "proj-1", Title: "Dep", Behavior: "dev"}
	if err := orchestrator.CreateTask(d.Conn, dep); err != nil {
		t.Fatalf("create dep: %v", err)
	}
	task := &orchestrator.Task{
		ProjectID: "proj-1",
		Title:     "Task",
		Behavior:  "dev",
		DependsOn: []string{dep.ID},
	}
	if err := orchestrator.CreateTask(d.Conn, task); err != nil {
		t.Fatalf("create task: %v", err)
	}

	// Delete the dependency — should also remove from task_dependencies
	if err := orchestrator.DeleteTask(d.Conn, dep.ID); err != nil {
		t.Fatalf("DeleteTask(dep) error = %v", err)
	}

	// task should now have no DependsOn
	got, err := orchestrator.GetTask(d.Conn, task.ID)
	if err != nil {
		t.Fatalf("GetTask() error = %v", err)
	}
	if len(got.DependsOn) != 0 {
		t.Fatalf("DependsOn = %v after dep deleted, want empty", got.DependsOn)
	}
}
